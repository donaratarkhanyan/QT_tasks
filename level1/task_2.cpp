#include <iostream>

// Data Types and Variables
int main()
{

	int Integer = 7;
	char Character = 'A';
	double Double = 3.14;
	float Float = 8.2f;

	std::cout << "Integer = " << Integer << std::endl;
	std::cout << "Character = " << Character << std::endl;
	std::cout << "Double = " << Double << std::endl;
	std::cout << "Float = " << Float << std::endl;
	return 0;
}
